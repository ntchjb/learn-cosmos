// OnRecvPacket implements the IBCModule interface
func (am AppModule) OnRecvPacket(
	ctx sdk.Context,
	packet channeltypes.Packet,
) (*sdk.Result, []byte, error) {
	// Unmarshal data in MsgRecvPacket
	var oracleResult oracletypes.OracleResponsePacketData
	if err := types.ModuleCdc.UnmarshalJSON(packet.GetData(), &oracleResult); err != nil {
		return nil, nil, sdkerrors.Wrapf(sdkerrors.ErrInvalidRequest, "unable to unmarshal oracle result: %w", err)
	}

	// Get order ID from client ID of the MsgRecvPacket
	clientIDList := strings.Split(oracleResult.ClientID, ":")
	if len(clientIDList) != 2 {
		return nil, nil, sdkerrors.Wrapf(sdkerrors.ErrInvalidRequest, "unable to get order ID from oracle's client ID")
	}
	orderID := strings.Split(oracleResult.ClientID, ":")[1]

	var obiOutput types.GoldPriceOBIOutput
	obi.MustDecode(oracleResult.Result, &obiOutput)
	if err := am.keeper.ProcessOrder(ctx, orderID, obiOutput.Price); err != nil {
		return nil, nil, err
	}

	return &sdk.Result{
		Events: ctx.EventManager().Events().ToABCIEvents(),
	}, nil, nil
}

